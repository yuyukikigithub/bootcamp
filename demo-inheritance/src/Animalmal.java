public abstract class Animalmal { // abstract, to be parent, cannot be new
    private int age;

    public Animalmal(){
        
    }

    public Animalmal(int age){
        this.age=age;
    }

    public int getAge(){
        return this.age;
    }

    public void setAge(int age){
        this.age=age;
    }


}
